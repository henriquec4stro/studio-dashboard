export function analyzeSentiment(word: string): { score: number; category: "positive" | "negative" | "neutral" } {
  const normalizedWord = word.toLowerCase().trim()
  const score = PORTUGUESE_SENTIMENT_LEXICON[normalizedWord] || 0

  let category: "positive" | "negative" | "neutral"
  if (score > 0) {
    category = "positive"
  } else if (score < 0) {
    category = "negative"
  } else {
    category = "neutral"
  }

  return { score, category }
}

export function getSentimentColor(category: "positive" | "negative" | "neutral", intensity = 1): string {
  const alpha = Math.min(0.9, Math.max(0.4, intensity * 0.3 + 0.4))

  switch (category) {
    case "positive":
      return `rgba(34, 197, 94, ${alpha})` // Green
    case "negative":
      return `rgba(239, 68, 68, ${alpha})` // Red
    case "neutral":
    default:
      return `rgba(107, 114, 128, ${alpha})` // Gray
  }
}

export function calculateOverallSentiment(words: Array<{ word: string; count: number }>): {
  overall: "positive" | "negative" | "neutral"
  score: number
  distribution: {
    positive: number
    negative: number
    neutral: number
  }
  weightedScore: number
} {
  let totalScore = 0
  let totalWeight = 0
  let positiveCount = 0
  let negativeCount = 0
  let neutralCount = 0

  words.forEach(({ word, count }) => {
    const sentiment = analyzeSentiment(word)
    const weight = count

    totalScore += sentiment.score * weight
    totalWeight += weight

    if (sentiment.category === "positive") {
      positiveCount += count
    } else if (sentiment.category === "negative") {
      negativeCount += count
    } else {
      neutralCount += count
    }
  })

  const weightedScore = totalWeight > 0 ? totalScore / totalWeight : 0
  const averageScore = words.length > 0 ? totalScore / words.length : 0

  let overall: "positive" | "negative" | "neutral"
  if (weightedScore > 0.1) {
    overall = "positive"
  } else if (weightedScore < -0.1) {
    overall = "negative"
  } else {
    overall = "neutral"
  }

  const total = positiveCount + negativeCount + neutralCount

  return {
    overall,
    score: averageScore,
    weightedScore,
    distribution: {
      positive: total > 0 ? Math.round((positiveCount / total) * 100) : 0,
      negative: total > 0 ? Math.round((negativeCount / total) * 100) : 0,
      neutral: total > 0 ? Math.round((neutralCount / total) * 100) : 0,
    },
  }
}
